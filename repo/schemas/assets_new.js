const mongoose = require("mongoose");

const NewAssetsSchema = mongoose.Schema({
  AssestName: {
    type: String,
    required: true,
    trim: true,
  },
  quantity: {
    type: Number,
    required: true,
    trim: true,
  },
  cost: {
    type: String,
    required: true,
    trim: true,
  },
  userName: {
    type: String,
    required: true,
    trim: true,
  },
});
module.exports = mongoose.model("Assets", NewAssetsSchema);
